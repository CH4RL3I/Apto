// One-off: parse cs-*.md frontmatter + body into a TS data file,
// and emit a SQL seed that creates a demo auth user + public.users row +
// public.companies row + public.case_studies row for every case study.
import { createHash } from "node:crypto";
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const SRC_DIRS = [
  "/Users/emiliogappa/Desktop/apto case studies",
  "/Users/emiliogappa/Desktop/apto case studies/case studies 2",
];
const OUT_TS = "/Users/emiliogappa/Desktop/Apto/src/lib/questionnaire/case-studies.ts";
const OUT_SQL = "/Users/emiliogappa/Desktop/Apto/supabase/004_case_study_seed.sql";

function parseFrontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!m) throw new Error("no frontmatter");
  const head = m[1];
  const body = m[2].trim();

  const meta = {};
  const lines = head.split("\n");
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    const kv = line.match(/^([a-z_]+):\s*(.*)$/);
    if (!kv) { i++; continue; }
    const key = kv[1];
    const rest = kv[2];

    if (rest === "") {
      const items = [];
      i++;
      while (i < lines.length && lines[i].startsWith("  - ")) {
        items.push(lines[i].slice(4).trim());
        i++;
      }
      meta[key] = items;
    } else {
      let v = rest.trim();
      if (
        (v.startsWith('"') && v.endsWith('"')) ||
        (v.startsWith("'") && v.endsWith("'"))
      ) {
        v = v.slice(1, -1);
      }
      meta[key] = v;
      i++;
    }
  }
  return { meta, body };
}

function extractCompanyName(body) {
  const imgAlt = body.match(/<img[^>]*alt="([^"]+?)\s+logo"/i);
  if (imgAlt) return imgAlt[1].trim();
  const bold = body.match(/\*\*([^*\n]{2,80})\*\*/);
  return bold ? bold[1].trim() : null;
}

// Deterministic UUIDv5-ish: SHA-1 of namespace + id, formatted as a uuid.
// We don't need full RFC 4122 compliance here — just a stable mapping from
// case-study slug to a UUID for both auth.users and public.companies.
function deterministicUuid(namespace, id) {
  const h = createHash("sha1").update(`${namespace}:${id}`).digest("hex");
  return [
    h.slice(0, 8),
    h.slice(8, 12),
    "5" + h.slice(13, 16),
    "8" + h.slice(17, 20),
    h.slice(20, 32),
  ].join("-");
}

function sqlString(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlTextArray(arr) {
  if (!arr || arr.length === 0) return "'{}'";
  const escaped = arr.map((v) => `"${String(v).replace(/"/g, '\\"')}"`);
  return `'{${escaped.join(",")}}'`;
}

const files = SRC_DIRS.flatMap((dir) =>
  readdirSync(dir)
    .filter((f) => /^cs-\d+.*\.md$/.test(f))
    .map((f) => join(dir, f)),
).sort((a, b) => {
  const idA = Number(a.match(/cs-(\d+)/)?.[1] ?? 0);
  const idB = Number(b.match(/cs-(\d+)/)?.[1] ?? 0);
  return idA - idB;
});

const cases = files.map((path) => {
  const raw = readFileSync(path, "utf8");
  const { meta, body } = parseFrontmatter(raw);
  const companyName = extractCompanyName(body);
  return {
    id: meta.id,
    title: meta.title,
    cluster: meta.cluster,
    duration: meta.duration,
    estimatedMinutes: Number(meta.estimated_minutes),
    matchesRoles: meta.matches_roles || [],
    matchesFields: meta.matches_fields || [],
    matchesIndustries: meta.matches_industries || [],
    matchesMode: meta.matches_mode,
    matchesOutput: meta.matches_output,
    skillsTested: meta.skills_tested || [],
    companyName,
    logoUrl: `/company-logos/${meta.id}.png`,
    body,
  };
});

console.log(`Parsed ${cases.length} case studies.`);

for (const c of cases) {
  if (!c.id || !c.title || !c.matchesMode || !c.matchesOutput) {
    console.error("Missing fields in", c.id, c);
  }
  if (!c.companyName) {
    console.warn("No company name extracted for", c.id);
  }
}

const tsHeader = `// AUTO-GENERATED from ../../../apto case studies/cs-*.md.
// Do not edit by hand — re-run scripts/build-case-studies.mjs.
import type { Job } from "./matching";

export type CaseStudyDuration = "short" | "medium" | "long";

export interface CaseStudy {
  id: string;
  title: string;
  cluster: string;
  duration: CaseStudyDuration;
  estimatedMinutes: number;
  matchesRoles: string[];
  matchesFields: string[];
  matchesIndustries: string[];
  matchesMode: Job["mode"];
  matchesOutput: Job["output"];
  skillsTested: string[];
  companyName: string | null;
  logoUrl: string;
  body: string;
}

export const CASE_STUDIES: CaseStudy[] = ${JSON.stringify(cases, null, 2)};
`;

writeFileSync(OUT_TS, tsHeader);
console.log(`Wrote ${OUT_TS}`);

// ---- SQL seed ---------------------------------------------------------------

const DEMO_PASSWORD = "apto-demo-2025";

const sqlLines = [
  "-- AUTO-GENERATED by scripts/build-case-studies.mjs.",
  `-- Seeds ${cases.length} demo company accounts and case-study rows. Idempotent.`,
  "-- Run AFTER schema.sql, 002, 003.",
  `-- All ${cases.length} demo companies share the password: ${DEMO_PASSWORD}`,
  "",
  "create extension if not exists \"uuid-ossp\";",
  "create extension if not exists \"pgcrypto\";",
  "",
];

const authUserRows = [];
const authIdentityRows = [];
const publicUserRows = [];
const companyRows = [];
const caseStudyRows = [];

for (const c of cases) {
  const userId = deterministicUuid("apto-demo-auth-user", c.id);
  const companyId = deterministicUuid("apto-demo-company", c.id);
  const identityId = deterministicUuid("apto-demo-identity", c.id);
  const email = `demo+${c.id}@apto.test`;
  const companyName = c.companyName || c.title;
  const logoUrl = c.logoUrl;
  const identityData = JSON.stringify({ sub: userId, email, email_verified: true });

  authUserRows.push(
    `  (${sqlString(userId)}, ${sqlString(email)}, crypt(${sqlString(DEMO_PASSWORD)}, gen_salt('bf')), '00000000-0000-0000-0000-000000000000', 'authenticated', 'authenticated', now(), now(), now(), '{"provider":"email","providers":["email"]}'::jsonb, '{}'::jsonb)`,
  );
  authIdentityRows.push(
    `  (${sqlString(identityId)}, ${sqlString(userId)}, ${sqlString(userId)}, ${sqlString(identityData)}::jsonb, 'email', now(), now(), now())`,
  );
  publicUserRows.push(
    `  (${sqlString(userId)}, 'company', ${sqlString(email)}, ${sqlString(companyName)})`,
  );
  companyRows.push(
    `  (${sqlString(companyId)}, ${sqlString(userId)}, ${sqlString(companyName)}, ${sqlString(logoUrl)}, ${sqlString(email)})`,
  );
  caseStudyRows.push(
    `  (${sqlString(c.id)}, ${sqlString(companyId)}, ${sqlString(c.title)}, ${sqlString(c.body)}, ${c.estimatedMinutes}, 'intermediate', ${sqlTextArray(c.skillsTested)})`,
  );
}

sqlLines.push("-- 1. Demo auth.users (one per case study), email + bcrypt password.");
sqlLines.push(
  "insert into auth.users (id, email, encrypted_password, instance_id, aud, role, email_confirmed_at, created_at, updated_at, raw_app_meta_data, raw_user_meta_data) values",
);
sqlLines.push(
  authUserRows.join(",\n") +
    "\non conflict (id) do update set\n" +
    "  encrypted_password = excluded.encrypted_password,\n" +
    "  email_confirmed_at = excluded.email_confirmed_at;",
);
sqlLines.push("");

sqlLines.push("-- 2. auth.identities so Supabase can resolve email → user during sign-in.");
sqlLines.push(
  "insert into auth.identities (id, user_id, provider_id, identity_data, provider, last_sign_in_at, created_at, updated_at) values",
);
sqlLines.push(
  authIdentityRows.join(",\n") +
    "\non conflict (provider, provider_id) do nothing;",
);
sqlLines.push("");

sqlLines.push("-- 3. public.users mirroring those auth users with role='company'.");
sqlLines.push("insert into public.users (id, role, email, name) values");
sqlLines.push(publicUserRows.join(",\n") + "\non conflict (id) do nothing;");
sqlLines.push("");

sqlLines.push("-- 4. public.companies, one per case study.");
sqlLines.push(
  "insert into public.companies (id, user_id, name, logo_url, contact_email) values",
);
sqlLines.push(
  companyRows.join(",\n") +
    "\non conflict (id) do update set\n" +
    "  user_id = excluded.user_id,\n" +
    "  name = excluded.name,\n" +
    "  logo_url = excluded.logo_url,\n" +
    "  contact_email = excluded.contact_email;",
);
sqlLines.push("");

sqlLines.push("-- 5. public.case_studies linked to the demo companies.");
sqlLines.push(
  "insert into public.case_studies (id, company_id, title, brief, time_minutes, difficulty, skills_tested) values",
);
sqlLines.push(
  caseStudyRows.join(",\n") +
    "\non conflict (id) do update set\n" +
    "  company_id = excluded.company_id,\n" +
    "  title = excluded.title,\n" +
    "  brief = excluded.brief,\n" +
    "  time_minutes = excluded.time_minutes,\n" +
    "  skills_tested = excluded.skills_tested;",
);
sqlLines.push("");

writeFileSync(OUT_SQL, sqlLines.join("\n"));
console.log(`Wrote ${OUT_SQL}`);
